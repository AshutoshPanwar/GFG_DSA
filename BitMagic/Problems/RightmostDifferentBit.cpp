class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        // Your code here
        if(m == n)
            return -1;
        int x = m^n;
        int count = 0;
        while(x > 0){
            count++;
            if((x%2) == 1){
                return count;
            }
            x=x>>1;
        }
    }
};

